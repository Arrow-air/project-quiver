"""BOM 2200 - Battery Slider subassembly.

Battery mounting and quick-release mechanism.

Expected STEP files in steps/:
    2211_battery_slider.step    Battery slider x2
"""

from pathlib import Path

from build123d import Compound

from quiver.common import load_all_steps, load_vendor_steps

_DIR = Path(__file__).parent


def make_assembly() -> Compound | None:
    """Build the battery slider subassembly from imported STEP files."""
    parts = list(load_all_steps(_DIR).values())
    parts += list(load_vendor_steps(_DIR).values())
    if not parts:
        return None
    return Compound(children=parts)
